package dio.v_aguiar.sintaxe;

public class TiposVariaveis {
    public static void main(String[] args) {
        // tipos variáveis
        // float precisa terminar com "f" maiúsculo ou minúsculo
        // long precisa terminar com "l" maiúsculo ou minúsculo

        long cpf = 12312312388L;
        float pi = 3.14F;

        System.out.println(cpf);
        System.out.println(pi);
    }
}
